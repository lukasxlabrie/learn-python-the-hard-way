# Prompt the user to enter their age and store it as a string in the variable 'age'
age = input("How old are you? ")

# Prompt the user to enter their height and store it in 'height'
height = input("How tall are you? ")

# Prompt the user to enter their weight and store it in 'weight'
weight = input("How much do you weigh? ")

# Use an f-string to insert the user's answers into a formatted sentence
print(f"So, you're {age} old, {height} tall, and {weight} heavy.")
